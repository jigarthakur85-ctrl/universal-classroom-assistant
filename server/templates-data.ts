/**
 * Get templates for specific class and subject
 */
export function getTemplatesForClassAndSubject(classLevel: string, subject: string) {
  return CBSE_TEMPLATES.filter(
    (t) => t.class === classLevel && t.subject === subject
  );
}

/**
 * Get all classes available
 */
export function getAvailableClasses() {
  const classes = new Set(CBSE_TEMPLATES.map((t) => t.class));
  return Array.from(classes).sort();
}

/**
 * Get all subjects for a class
 */
export function getSubjectsForClass(classLevel: string) {
  const subjects = new Set(
    CBSE_TEMPLATES.filter((t) => t.class === classLevel).map((t) => t.subject)
  );
  return Array.from(subjects).sort();
}

/**
 * Get all topics for a class and subject
 */
export function getTopicsForClassAndSubject(classLevel: string, subject: string) {
  const topics = new Set(
    CBSE_TEMPLATES.filter(
      (t) => t.class === classLevel && t.subject === subject
    ).map((t) => t.topic)
  );
  return Array.from(topics).sort();
}
